"""Canonical CrateDB schema for the structured ``velop.*`` tables.

The watcher no longer writes to CrateDB directly. Records flow watcher -> Kafka
(Confluent-Avro) -> Kafka Connect JDBC sinks -> CrateDB, so the tables must
**pre-exist** (the sinks run ``auto.create=false``). This module is the single
source of truth for each table's column order and CrateDB type:

- ``kafka_sink.TABLE_SPECS`` mirrors :data:`TABLES` (asserted in tests), and
- ``sql/velop_schema.sql`` is generated by :func:`schema_sql` (run
  ``python -m velop_watcher.schema`` to regenerate it).

``id``/``snapshot_id``/``fetched_at`` are prepended to every table and are not
listed per-table here. Only the 11 structured tables live here; the raw_text
dumps (``sysinfo``/``node_sysinfo``) and the old ``oui`` cache are gone with the
direct-write path.
"""

