def column_names(table: str) -> list[str]:
    """Ordered data-column names for ``table`` (excludes the id/meta prefix)."""
    return [name for name, _type in TABLES[table]]


def _col_sql(name: str) -> str:
    """Quote ``timestamp`` (a CrateDB reserved word); pass other names through."""
    return '"timestamp"' if name == "timestamp" else name


def create_table_sql(table: str) -> str:
    """``CREATE TABLE IF NOT EXISTS`` for one structured table."""
    cols = PK_PREFIX + tuple(TABLES[table])
    body = ",\n".join(f"    {_col_sql(name)} {ctype}" for name, ctype in cols)
    return f"CREATE TABLE IF NOT EXISTS velop.{table} (\n{body}\n)"


def schema_sql() -> str:
    """Full DDL for all structured tables, ready to apply to CrateDB."""
    header = (
        "-- velop structured-table schema -- GENERATED from velop_watcher/schema.py.\n"
        "-- Regenerate with:  python -m velop_watcher.schema > sql/velop_schema.sql\n"
        "--\n"
        "-- The watcher is Kafka-only; the Connect JDBC sinks land records into these\n"
        "-- tables, which must pre-exist (the sinks run auto.create=false). Apply once,\n"
        "-- e.g.  crash < sql/velop_schema.sql  (or psql, or the CrateDB admin UI).\n\n"
    )
    return header + ";\n\n".join(create_table_sql(t) for t in TABLES) + ";\n"


